document.getElementById('fechar222').addEventListener('click', function() {
    var listaDePesquisa = document.querySelector('.listaDePesquisa');
    var botao = document.getElementById('fechar222');

    if (listaDePesquisa.style.display === 'none') {
      listaDePesquisa.style.display = 'block';
      botao.classList.remove('mostrar');
      botao.classList.add('ocultar');
      botao.innerHTML = '<i class="fas fa-times"></i>';
    } else {
      listaDePesquisa.style.display = 'none';
      botao.classList.remove('ocultar');
      botao.classList.add('mostrar');
      botao.innerHTML = '<i class="fas fa-search"></i>';
    }
  });

  function toggleDiv() {
    var listaDePesquisa = document.querySelector('.listaDePesquisa');
    var botao = document.getElementById('fechar222');

    if (window.innerWidth > 800 || listaDePesquisa.style.display === 'block') {
      // Se a largura da tela for maior que 800 pixels ou a div já estiver visível
      listaDePesquisa.style.display = 'block';
      botao.classList.remove('mostrar');
      botao.classList.add('ocultar');
      botao.innerHTML = '<i class="fas fa-times"></i>';
    } else {
      listaDePesquisa.style.display = 'none';
      botao.classList.remove('ocultar');
      botao.classList.add('mostrar');
      botao.innerHTML = '<i class="fas fa-search"></i>';
    }
  }

  document.getElementById('fechar222').addEventListener('click', function() {
    toggleDiv();
  });

  // Verificar o tamanho da tela ao carregar e redimensionar
  window.addEventListener('resize', function() {
    toggleDiv();
  });

  // Chamar a função inicialmente ao carregar a página
  toggleDiv();